ADJ_DIGITS = 13

def product(s):
    prod = 1
    for c in s:
        prod *= int(c)
    return prod

def solve(N = NUM, ADJ_DIGITS = ADJ_DIGITS):
    # sliding window TC: O(N) SC: O(1)
    return max(product(N[i:i+ADJ_DIGITS]) for i in range(len(N)-ADJ_DIGITS+1))

